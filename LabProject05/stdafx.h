	inline int ReadStringFromFile(std::ifstream& iFile, std::string& str)
	{
		int strLength = 0;
		iFile.read((char*)&strLength, sizeof(int));
		str.resize(strLength);
		iFile.read(&str[0], strLength);
		return strLength;
	}

