#pragma once

enum class ShaderNum{ Diffused=0, Illuminated, Terrain, AnimationWireframe, Standard, StaticMesh, SkyBox };

class CGameObject;
class CCamera;

class CShader
{
public:
	CShader();
	virtual ~CShader();

protected:
	// 이 쉐이더의 쉐이더 넘버
	int	m_nShader = -1;

	std::list<CGameObject*> render_list_;

public:

	int GetShaderNum(){ return m_nShader; }

	virtual D3D12_INPUT_LAYOUT_DESC CreateInputLayout();
	virtual D3D12_RASTERIZER_DESC CreateRasterizerState();
	virtual D3D12_BLEND_DESC CreateBlendState();
	virtual D3D12_DEPTH_STENCIL_DESC CreateDepthStencilState();

	virtual D3D12_SHADER_BYTECODE CreateVertexShader(ID3DBlob** ppd3dShaderBlob);
	virtual D3D12_SHADER_BYTECODE CreatePixelShader(ID3DBlob** ppd3dShaderBlob);

	D3D12_SHADER_BYTECODE CompileShaderFromFile(WCHAR* pszFileName, LPCSTR pszShaderName, LPCSTR pszShaderProfile, ID3DBlob** ppd3dShaderBlob);
	D3D12_SHADER_BYTECODE ReadCompiledShaderFromFile(WCHAR* pszFileName, ID3DBlob** ppd3dShaderBlob = NULL);

	virtual void CreateShader(ID3D12Device* pd3dDevice, ID3D12RootSignature* pd3dGraphicsRootSignature);

	virtual void OnPrepareRender(ID3D12GraphicsCommandList* pd3dCommandList);
	virtual void Render(ID3D12GraphicsCommandList* pd3dCommandList, CCamera* camera, float elapsed_time);

	void AddObject(CGameObject* object) { render_list_.push_back(object); }

protected:
	ID3D12PipelineState** m_ppd3dPipelineStates = NULL;
	int					m_nPipelineStates = 0;

};

